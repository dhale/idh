  private static double poly(double[] c, double x) {
    int i = c.length-1;
    double sum = c[i];
    for (--i; i>=0; --i) {
      sum *= x;
      sum += c[i];
    }
    return sum;
  }

  private static double sign(double x) {
    if (x>0.0) return 1.0;
    else if (x<0.0) return -1.0;
    else return 0.0;
  }

  private static double rational(double[] num, double[] den, double z) {
    int n = num.length;
    double s1,s2;
    if (z<=1.0) {
      s1 = num[n-1];
      s2 = den[n-1];
      for (int i=n-2; i>=0; --i) {
        s1 *= z;
        s2 *= z;
        s1 += num[i];
        s2 += den[i];
      }
    } else {
      z = 1.0/z;
      s1 = num[0];
      s2 = den[0];
      for (int i=1; i<n; ++i) {
        s1 *= z;
        s2 *= z;
        s1 += num[i];
        s2 += den[i];
      }
    }
    return s1/s2;
  }

